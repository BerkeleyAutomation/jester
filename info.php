<h4>Eigentaste Background:</h4>
<p>
Eigentaste is a collaborative filtering algorithm that uses universal queries to elicit real-valued user ratings on a common set of items and applies principal component analysis (PCA) to the resulting dense subset of the ratings matrix. PCA facilitates dimensionality reduction for offline clustering of users and rapid computation of recommendations.
</p>
<p>
For more information about Eigentaste, please see the following paper:
</p>
<p>
<i><a href="http://www.ieor.berkeley.edu/~goldberg/pubs/Eigentaste-Info-Retrieval-Journal.pdf" target="_blank">Eigentaste: A Constant Time Collaborative Filtering Algorithm</a>,</i>
<br />
<a href="http://goldberg.berkeley.edu" target="_blank">Ken Goldberg</a>, Theresa Roeder, Dhruv Gupta, and Chris Perkins, <b>Information Retrieval Journal</b>, 4(2), pp. 133-151. July 2001.
</p>
<h4>New Research:</h4>
<p>
Eigentaste 5.0 is the latest version of Eigentaste, which improves upon the original algorithm by dynamically adapting the order that items are recommended. It does this by integrating user clustering with item clustering and monitoring item portfolio effects.
</p>
<p>
For more information about Eigentaste 5.0, please see the following paper:
</p>
<p>
<i><a href="http://www.ieor.berkeley.edu/~goldberg/pubs/tavi-recsys2007-spap122s1-nathanson.pdf" target="_blank">Eigentaste 5.0: Constant-Time Adaptability in a Recommender System Using Item Clustering</a>,</i> 
<br />
Tavi Nathanson, <a href="http://www.ocf.berkeley.edu/~ebitton/" target="_blank">Ephrat Bitton</a>, and <a href="http://goldberg.berkeley.edu" target="_blank">Ken Goldberg</a>. Working Paper Track. <b>ACM Conference on Recommender Systems (RecSys)</b>, Minneapolis, MN, Oct 2007.
</p>
<h4>Applications:</h4>
<p>
Eigentaste was patented by UC Berkeley in 2003. It has many possible applications, such as the recommendation of books, movies, toys, stocks, and music.
</p>
<p>
It was originally used in an online joke recommendation system called <a href="http://eigentaste.berkeley.edu" target="_blank">Jester</a>, which recommends new jokes to users based on their ratings of an initial set.
</p>
<p>
The <a href="http://eigentaste.berkeley.edu" target="_blank">Jester</a> dataset is freely available for research use, when referenced. It is downloadable at:
</p>
<p>
<a href="http://eigentaste.berkeley.edu/jester-data" target="_blank">http://eigentaste.berkeley.edu/jester-data</a>
</p>
<p>
A <a href="http://dd.berkeley.edu/user/about.php" target="_blank">team</a> at UC Berkeley's <a href="http://automation.berkeley.edu" target="_blank">Automation Lab</a> recently launched a new application called <a href="http://dd.berkeley.edu" target="_blank">Donation Dashboard</a>, which recommends a donation portfolio to you.
</p>
